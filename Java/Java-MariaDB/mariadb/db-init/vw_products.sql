USE classicmodels;

CREATE OR REPLACE VIEW vw_products
AS

	/*********************************************************************************************************
	Vue 'vw_products'
	----------------------------------------------------------------------------------------------------------
	Résumé      :	Vue des produits.
	----------------------------------------------------------------------------------------------------------
	Description :	...
	----------------------------------------------------------------------------------------------------------
	/!\ NOTE /!\:	...
	----------------------------------------------------------------------------------------------------------
	Dépendances	:	...

	*********************************************************************************************************/

	SELECT productCode
		 , productName
		 , productLine
		 , productScale
		 , productVendor
		 , productDescription
		 , quantityInStock
		 , buyPrice
		 , MSRP
	FROM products;
;
